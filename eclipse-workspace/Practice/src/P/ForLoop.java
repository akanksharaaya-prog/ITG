package P;

public class ForLoop {
	public static void main(String args[]) {
			for (int i = 1; i <= 21; i++) {
			    System.out.print(i+" ");
			    }
			System.out.println("\n");
		for (char i = 'A'; i <= 'Z'; i++) {
			System.out.print(i + " ");
		}
		System.out.println("\n");
		for (char ch = 'a'; ch <= 'z'; ch++) {
			System.out.print(ch+" ");
		}
	}
}
